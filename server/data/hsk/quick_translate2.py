def main():
    base_dir = r"e:\PYTHON PROJECT UNI\Ncwu Int. Community\Kimi_Agent_Build+Class+Schedule+Site\server\data\hsk"
    
    levels = ['1', '2', '3', '4', '5', '6', '7-9']
    
    total_translated = 0
    
    for level in levels:
        file_path = os.path.join(base_dir, f'hsk{level}_vocabulary.json')
        
        if not os.path.exists(file_path):
            print(f"File not found: {file_path}")
            continue
        
        print(f"\nProcessing: {file_path}")
        
        with open(file_path, 'r', encoding='utf-8') as f:
            vocabulary = json.load(f)
        
        translated = 0
        total = len(vocabulary)
        
        for word in vocabulary:
            if word['word'] in HSK_DICT:
                if not word.get('english') or word['english'] == '':
                    word['english'] = HSK_DICT[word['word']]
                    translated += 1
        
        with open(file_path, 'w', encoding='utf-8') as f:
            json.dump(vocabulary, f, ensure_ascii=False, indent=2)
        
        print(f"Level {level}: Translated {translated}/{total} words")
        total_translated += translated
    
    print(f"\n{'='*50}")
    print(f"TOTAL TRANSLATED: {total_translated} words")

if __name__ == '__main__':
    main()
