def translate_with_google(word):
    try:
        encoded = urllib.parse.quote(word)
        url = f"https://translate.googleapis.com/translate_a/single?client=gtx&sl=zh-CN&tl=en&dt=t&q={encoded}"
        req = urllib.request.Request(url, headers={
            'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'
        })
        with urllib.request.urlopen(req, timeout=10) as response:
            data = response.read().decode('utf-8')
            import re
            match = re.search(r'\[\["([^"]+)"', data)
            if match:
                return match.group(1)
        return None
    except Exception as e:
        return None

def process_vocabulary(file_path):
    print(f"\nProcessing: {file_path}")
    
    with open(file_path, 'r', encoding='utf-8') as f:
        vocabulary = json.load(f)
    
    total = len(vocabulary)
    from_dict = 0
    from_api = 0
    errors = 0
    
    for i, word in enumerate(vocabulary):
        if not word.get('english') or word['english'] == '' or word['english'] == '(not in dictionary)':
            if word['word'] in HSK_DICTIONARY:
                word['english'] = HSK_DICTIONARY[word['word']]
                from_dict += 1
                if from_dict <= 10 or i % 100 == 0:
                    sys.stdout.write(f"[{i+1}/{total}] {word['word']} -> {word['english']} (dict)\n")
                    sys.stdout.flush()
            else:
                translation = translate_with_google(word['word'])
                if translation and len(translation) > 0:
                    word['english'] = translation
                    from_api += 1
                    if from_api <= 10 or i % 100 == 0:
                        sys.stdout.write(f"[{i+1}/{total}] {word['word']} -> {translation} (API)\n")
                        sys.stdout.flush()
                else:
                    word['english'] = ''
                    errors += 1
                
                if (i + 1) % 15 == 0:
                    time.sleep(0.5)
    
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(vocabulary, f, ensure_ascii=False, indent=2)
    
    print(f"Total: {total} | From Dict: {from_dict} | From API: {from_api} | Errors: {errors}")
    return from_dict + from_api

