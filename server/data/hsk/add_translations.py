def add_english_to_vocabulary(input_file, output_file):
    """Add English meanings from dictionary to vocabulary file"""
    print(f"Processing: {input_file}")
    
    # Load existing vocabulary
    with open(input_file, 'r', encoding='utf-8') as f:
        vocabulary = json.load(f)
    
    total = len(vocabulary)
    translated = 0
    
    for i, word in enumerate(vocabulary):
        # Always look up in dictionary for better translation
        if word['word'] in HSK_DICTIONARY:
            new_english = HSK_DICTIONARY[word['word']]
            if word.get('english') != new_english:
                word['english'] = new_english
                sys.stdout.write(f"[{i+1}/{total}] {word['word']} -> {word['english']}\n")
                translated += 1
            else:
                sys.stdout.write(f"[{i+1}/{total}] {word['word']} -> (unchanged)\n")
        elif not word.get('english'):
            word['english'] = ''
            sys.stdout.write(f"[{i+1}/{total}] {word['word']} -> (not in dictionary)\n")
        else:
            sys.stdout.write(f"[{i+1}/{total}] {word['word']} -> (kept existing)\n")
        sys.stdout.flush()
    
    # Save updated vocabulary
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(vocabulary, f, ensure_ascii=False, indent=2)
    
    print(f"\nTranslated: {translated}/{total} words")
    print(f"Saved: {output_file}\n")

def main():
    base_dir = os.path.dirname(os.path.abspath(__file__))
    
    levels = ['1', '2', '3', '4', '5', '6', '7-9']
    
    for level in levels:
        input_file = os.path.join(base_dir, f'hsk{level}_vocabulary.json')
        output_file = os.path.join(base_dir, f'hsk{level}_vocabulary.json')
        
        if os.path.exists(input_file):
            add_english_to_vocabulary(input_file, output_file)
        else:
            print(f"File not found: {input_file}\n")
    
    print("Translation complete!")

if __name__ == '__main__':
    main()
